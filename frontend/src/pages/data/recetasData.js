// Función para obtener todas las recetas
export const obtenerTodasLasRecetas = () => {
  return recetasData;
};

// Función para obtener receta por ID
export const obtenerRecetaPorId = (id) => {
  return recetasData.find(receta => receta.id === parseInt(id));
};

// Función para obtener recetas por categoría
export const obtenerRecetasPorCategoria = (categoria) => {
  if (categoria === 'all') return recetasData;
  return recetasData.filter(receta => receta.category === categoria);
};

// Función para obtener categorías únicas
export const obtenerCategorias = () => {
  const categorias = ['all', ...new Set(recetasData.map(receta => receta.category))];
  return categorias;
};

// Función para obtener dificultades únicas
export const obtenerDificultades = () => {
  const dificultades = ['all', ...new Set(recetasData.map(receta => receta.difficulty))];
  return dificultades;
};

// Función para buscar recetas
export const buscarRecetas = (termino) => {
  const terminoLower = termino.toLowerCase();
  return recetasData.filter(receta => 
    receta.title.toLowerCase().includes(terminoLower) ||
    receta.description.toLowerCase().includes(terminoLower) ||
    receta.tags.some(tag => tag.toLowerCase().includes(terminoLower))
  );
};